
package Coding;


public class Pembelian {
    private int no_pembelian;
    private String item;
    private int harga;
    private int noRJ;

    public int getNo_pembelian() {
        return no_pembelian;
    }

    public void setNo_pembelian(int no_pembelian) {
        this.no_pembelian = no_pembelian;
    }

    public String getItem() {
        return item;
    }

    public void setItem(String item) {
        this.item = item;
    }

    public int getHarga() {
        return harga;
    }

    public void setHarga(int harga) {
        this.harga = harga;
    }

    public int getNoRJ() {
        return noRJ;
    }

    public void setNoRJ(int noRJ) {
        this.noRJ = noRJ;
    }


}
